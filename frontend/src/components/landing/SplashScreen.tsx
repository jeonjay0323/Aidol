interface SplashScreenProps {
  isVisible: boolean;
  onAnimationComplete?: () => void;
}

/**
 * AIdol splash screen with animated logo.
 *
 * Animation sequence:
 * 1. Letters appear one by one from bottom (stagger)
 * 2. Gradient sweeps left-to-right to colorize
 * 3. Logo settles with a subtle scale
 * 4. Entire screen fades out
 */
export function SplashScreen({
  isVisible,
  onAnimationComplete,
}: SplashScreenProps) {
  return (
    <AnimatePresence onExitComplete={onAnimationComplete}>
      {isVisible && (
        <motion.div
          key="splash"
          variants={fadeOutVariants}
          initial="visible"
          exit="exit"
          className="bg-base-100 fixed inset-0 z-50 flex items-center justify-center"
        >
          <motion.div
            variants={logoScaleVariants}
            initial="hidden"
            animate="visible"
          >
            <motion.svg
              width={184}
              height={56}
              viewBox="0 0 92 28"
              fill="none"
              xmlns="http://www.w3.org/2000/svg"
              variants={containerVariants}
              initial="hidden"
              animate="visible"
            >
              <defs>
                <linearGradient
                  id="splash-gradient"
                  x1="0"
                  y1="13.9355"
                  x2="91.6515"
                  y2="13.9355"
                  gradientUnits="userSpaceOnUse"
                >
                  <stop stopColor="#E70051" />
                  <stop offset="1" stopColor="#9E54FF" />
                </linearGradient>
                <clipPath id="splash-letters">
                  {LETTER_PATHS.flatMap((letter, li) =>
                    letter.paths.map((d, pi) => (
                      <path key={`${li}-${pi}`} d={d} />
                    )),
                  )}
                </clipPath>
              </defs>

              {/* Gray letters appearing one by one */}
              {LETTER_PATHS.map((letter, li) => (
                <motion.g key={li} variants={letterVariants}>
                  {letter.paths.map((d, pi) => (
                    <path
                      key={`${li}-${pi}`}
                      d={d}
                      className="fill-base-content/20"
                    />
                  ))}
                </motion.g>
              ))}

              {/* Gradient sweep overlay clipped to letter shapes */}
              <g clipPath="url(#splash-letters)">
                <motion.rect
                  width="92"
                  height="28"
                  fill="url(#splash-gradient)"
                  variants={gradientSweepVariants}
                  initial="hidden"
                  animate="visible"
                />
              </g>
            </motion.svg>
          </motion.div>
        </motion.div>
      )}
    </AnimatePresence>
  );
}
